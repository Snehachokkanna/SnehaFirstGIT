package javaPrograms;

public class Example24 {

	public static void main(String[] args)
	{
        int n1 = 31;

        int n2 = 031; //octal representation

        int n3 = 0x31; //hexadecimal representation

        int n4 = 0b1001; //Binary representation

        System.out.println("Integer (input 31): output is "+n1);

        System.out.println("Octal (input 031): output is "+n2);

        System.out.println("Hexadecimal (input 0x31): output is "+n3);

        System.out.println("Binary (input b1001): output is "+n4);





	}

}
