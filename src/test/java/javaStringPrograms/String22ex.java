package javaStringPrograms;



public class String22ex {

	public static void main(String[] args)
	{
		//char array to string
	
		char x[] = {'j','e','s','s','i','e'};
		String y = " ";
		y=y.valueOf(x);
		
		System.out.println(y);
		

		//OR
		char f[]= {'j','a','v','a'};
		String g = " ";
		for(int u=0;u<f.length;u++)
		{
			System.out.println(g);
		}
		

	}

}
